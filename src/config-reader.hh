#ifndef CONFIG_READER_HH
#define CONFIG_READER_HH

#include <xercesc/dom/DOM.hpp>
#include <abstract-config-reader.hh>
#include <config.hh>

class Config_reader : public Abstract_config_reader
{
public:
  Config_reader(Config *config);
  virtual ~Config_reader();
protected:
  virtual void parse_document(const xercesc::DOMElement *elem_config);
  virtual void print_config() const;
private:
  Config *_config;
  void parse_devices(const xercesc::DOMElement *elem_devices);
  void parse_logging(const xercesc::DOMElement *elem_logging);
  void parse_kiosk_mode(const xercesc::DOMElement *elem_kiosk_mode);
  void parse_key_bindings(const xercesc::DOMElement *elem_key_bindings);

  void parse_action(const xercesc::DOMElement *elem_action);
  void parse_keys(const xercesc::DOMElement *elem_keys,
                  const IKey_bindings::Action action);
  void parse_key(const xercesc::DOMElement *elem_key,
                 const IKey_bindings::Action action);
  void parse_audio(const xercesc::DOMElement *elem_audio);
  void parse_serial_parity_bit(const xercesc::DOMElement *elem_parity_bit);
  const XMLCh *parse_path(const xercesc::DOMElement *elem_device);
};

#endif /* CONFIG_READER_HH */

