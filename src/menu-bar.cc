#include <menu-bar.hh>
#include <log.hh>

Menu_bar::Menu_bar(Qt_actions *actions, QWidget *parent)
  : QMenuBar(parent)
{
  _file_menu = new File_menu(actions, this);
  if (!_file_menu) {
    Log::fatal("Menu_bar::Menu_bar(): not enough memory");
  }
  addMenu(_file_menu);

  _help_menu = new Help_menu(actions, this);
  if (!_help_menu) {
    Log::fatal("Menu_bar::Menu_bar(): not enough memory");
  }
  addMenu(_help_menu);
}

Menu_bar::~Menu_bar()
{
  // locally managed Qt objects
  _file_menu->deleteLater();
  _file_menu = 0;
  _help_menu->deleteLater();
  _help_menu = 0;
}

