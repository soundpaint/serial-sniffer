#include <control-panel.hh>
#include <log.hh>

Control_panel::Control_panel(App_control *app_control, QWidget *parent)
  : QWidget(parent)
{
  if (!app_control) {
    Log::fatal("Control_panel::Control_panel(): app_control is NULL");
  }
  _app_control = app_control;

  _layout = new QVBoxLayout();
  if (!_layout) {
    Log::fatal("Control_panel::Control_panel(): not enough memory");
  }
  setLayout(_layout);

  _control_console_logger_panel =
    new Control_console_logger_panel(_app_control, this);
  if (!_control_console_logger_panel) {
    Log::fatal("Control_panel::Control_panel(): not enough memory");
  }
  _layout->addWidget(_control_console_logger_panel);

  _control_file_logger_panel =
    new Control_file_logger_panel(_app_control, this);
  if (!_control_file_logger_panel) {
    Log::fatal("Control_panel::Control_panel(): not enough memory");
  }
  _layout->addWidget(_control_file_logger_panel);

  _control_serial_ports_panel =
    new Control_serial_ports_panel(_app_control, this);
  if (!_control_serial_ports_panel) {
    Log::fatal("Control_panel::Control_panel(): not enough memory");
  }
  _layout->addWidget(_control_serial_ports_panel);

  create_actions();
}

Control_panel::~Control_panel()
{
  // locally managed Qt objects
  _layout->deleteLater();
  _layout = 0;
  _control_console_logger_panel->deleteLater();
  _control_console_logger_panel = 0;
  _control_file_logger_panel->deleteLater();
  _control_file_logger_panel = 0;
  _control_serial_ports_panel->deleteLater();
  _control_serial_ports_panel = 0;

  // elsewhere managed objects
  _app_control = 0;
}

ILogger_status_listener *
Control_panel::get_logger_control_panel()
{
  return _control_console_logger_panel;
}

